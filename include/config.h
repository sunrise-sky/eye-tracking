/**
 * @file config.h
 * @brief 眼动追踪系统全局配置
 *
 * 本文件包含系统级别的配置参数，包括图像分辨率、
 * 算法阈值、通信参数等。
 */

#ifndef CONFIG_H
#define CONFIG_H

/* =====================================================================
 * 目标硬件平台
 * ===================================================================== */
#define TARGET_STM32F407

/* =====================================================================
 * 系统时钟
 * ===================================================================== */
#define SYS_CLOCK_HZ      168000000UL   /**< 系统时钟 168 MHz */
#define APB1_CLOCK_HZ      42000000UL   /**< APB1 总线时钟 */
#define APB2_CLOCK_HZ      84000000UL   /**< APB2 总线时钟 */

/* =====================================================================
 * 图像参数
 * ===================================================================== */
#define IMAGE_WIDTH       320            /**< 图像宽度（像素） */
#define IMAGE_HEIGHT      240            /**< 图像高度（像素） */
#define IMAGE_SIZE        (IMAGE_WIDTH * IMAGE_HEIGHT)  /**< 图像总像素数 */

/* =====================================================================
 * OV7670 摄像头配置
 * ===================================================================== */
#define OV7670_I2C_ADDR   0x42          /**< OV7670 写地址（7 位地址为 0x21） */
#define OV7670_XCLK_HZ    24000000UL    /**< 输入时钟 24 MHz */

/* DCMI 引脚映射（STM32F407） */
#define DCMI_HSYNC_PIN    GPIO_PIN_4    /**< PA4 - DCMI_HSYNC */
#define DCMI_VSYNC_PIN    GPIO_PIN_7    /**< PB7 - DCMI_VSYNC */
#define DCMI_PIXCLK_PIN   GPIO_PIN_6    /**< PA6 - DCMI_PIXCLK */
/* 数据线 D0-D7: PC6 PC7 PE0 PE1 PE4 PB6 PE5 PE6 */

/* =====================================================================
 * 眼动追踪算法参数
 * ===================================================================== */
/** 瞳孔检测灰度阈值（0-255），低于此值认为是瞳孔暗区 */
#define PUPIL_THRESHOLD       50

/** 最小瞳孔面积（像素数），用于过滤噪声 */
#define PUPIL_MIN_AREA        100

/** 最大瞳孔面积（像素数），用于过滤过大区域 */
#define PUPIL_MAX_AREA        8000

/** 连通区域分析最大候选数量 */
#define MAX_BLOBS             16

/** 瞳孔圆形度阈值（0.0 - 1.0），越接近 1 越圆 */
#define PUPIL_CIRCULARITY_MIN 0.55f

/** 标定点数量（默认 9 点标定） */
#define CALIB_POINT_COUNT     9

/** 标定采样帧数（每个标定点采集帧数） */
#define CALIB_SAMPLE_FRAMES   30

/* =====================================================================
 * UART 通信配置
 * ===================================================================== */
#define UART_BAUDRATE         115200UL  /**< 串口波特率 */
#define UART_TX_PIN           GPIO_PIN_9  /**< PA9 - USART1_TX */
#define UART_RX_PIN           GPIO_PIN_10 /**< PA10 - USART1_RX */

/** 注视点数据帧头 */
#define GAZE_FRAME_HEADER     0xAA
/** 注视点数据帧尾 */
#define GAZE_FRAME_TAIL       0x55

/* =====================================================================
 * 调试配置
 * ===================================================================== */
#ifdef DEBUG
#  define DBG_UART_BAUDRATE   921600UL
#endif

/* =====================================================================
 * 版本信息
 * ===================================================================== */
#define FIRMWARE_VERSION_MAJOR  1
#define FIRMWARE_VERSION_MINOR  0
#define FIRMWARE_VERSION_PATCH  0

#endif /* CONFIG_H */
